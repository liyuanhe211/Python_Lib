class Correction_Factor:
    def __init__(self,input_list):
        '''
            list: ['XB1K', '6-31+G(d,p)', 'Truhlar', '0.952', '0.927',"",""]
            order of numbers Zero Point Energy (ZPE) Fundamentals (F)	H(T)-H(0)	S(T)
            if "" present, means not avail, which was set to -1
        '''
        input_list += ['']*(7-len(input_list))

        self.method = input_list[0]
        self.basis = input_list[1]
        self.source = input_list[2]

        correctors = input_list[3:]
        correctors = [float(x) if x!="" else -1 for x in correctors]

        self.ZPE, self.fundamental, self.H_0_to_T, self.S = correctors
        self.numbers =  [self.ZPE, self.fundamental, self.H_0_to_T, self.S]

    def combine(self,other):
        assert isinstance(other,Correction_Factor)

        if [self.method,self.basis,self.source] == [other.method,other.basis,other.source]:
            for count in range(4):
                if self.numbers[count]!=-1 and other.numbers[count]!=-1:
                    return False

            for count,item in enumerate(other.numbers):
                if item!=-1:
                    assert self.numbers[count]==-1,str(self.numbers)+'\n'+str(other.numbers)
                    self.numbers[count]=item

            return True

        else:
            return False

    def list(self):
        return [self.method,self.basis,self.source]+self.numbers

    def __hash__(self):
        return hash(self.list())

    def __str__(self):
        return self.method+'\t'+self.basis+'\t'+self.source+'\t'+'\t'.join([str(x) for x in self.numbers])

