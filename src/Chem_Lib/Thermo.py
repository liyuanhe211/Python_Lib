    def __str__(self):
        ret = []
        for item in self.total_dict.items():
            ret.append(repr(item))
        ret.sort()
        return "\n".join(ret)

    def scaling(self):
        self.total()
        T = self.temp
        total = self.total_dict
        total['ZPE Factor'] = self.ZPE_factor
        total['S Factor'] = self.S_factor
        total['H Factor'] = self.H_factor
        total['Temp'] = self.temp
        total['Pressure'] = self.pressure
        total['Factor Source'] = self.factor_source

        total['ZPE_Corr'] =total['ZPE']*self.ZPE_factor
        total['H(0->T)_Corr'] = total['H(0->T)']*self.H_factor
        total['S(T)_Corr'] = total['S(T)']*self.S_factor
        total['G(T)_Corr'] = total['ZPE_Corr'] + total['H(0->T)_Corr'] - T*total['S(T)_Corr']

        self.total_dict = total

