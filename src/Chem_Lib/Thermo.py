class Thermo_calculation:
    def __init__(self,input_freq_step:Gaussian_output_step,factor_database = None,
                 manual_specify = False,
                 temp=-1.0,pressure=-1.0,
                 ZPE_factor=1.0, H_factor=1.0, S_factor=1.0, factor_source='Not Scaled'
                 ):
        '''

        :param input_freq_step:
        :param temp: temperature in K
        :param pressure: pressure in Pa
        :param method: QM method for scaling factor
        :param basis:  QM basis for scaling factor
        :param factor_database: a database for scaling factors, generated by gaussianbatchextract,
               should be a list of dict like:{'S(T)': 0.9656, 'Basis': ['pc-0','pc-0'], 'Fundamentals (F)': 'segmented', 'ZPE': 0.9698, 'H(T)-H(0)': 0.9912, 'Fund <1000': 1.021, 'Method': ['B2GP-PLYP','B2GPPLYP'], 'Source': '10.1002/jcc.23073', 'Fund >1000': 0.9464}
               Basis and Method is a two-tuple, each is the method/basis before/after unify

        :return:
        '''

        # all variable, if performed any calculation, is stored as SI unit
        # (variable read directly from Gaussian output doesn't apply)

        self.factor_database = factor_database

        if isinstance(input_freq_step,str):
            print("Implicit step in thermo calculation.")
            assert os.path.isfile(input_freq_step), "File not exist."
            input_freq_step = Gaussian_output(input_freq_step)
            pass

        if isinstance(input_freq_step,Gaussian_output):
            print("Implicit step in thermo calculation.")
            freq_step = [x for x in input_freq_step.steps if x.has_freq and x.normal_termination]
            assert freq_step, "No normal freq step."
            self.freq_step = freq_step[-1]

        if isinstance(input_freq_step,Gaussian_output_step):
            self.freq_step=input_freq_step


        self.molecule = self.freq_step.last_coord
        self.multiplicity = self.molecule.multiplicity
        self.isotopes = self.freq_step.isotopes
        self.mass = sum(self.isotopes)/N_A/1000
        self.is_linear = self.molecule.is_linear()

        self.rotation_symm_number = self.freq_step.rotation_symm_number
        self.moments_of_inertia = self.freq_step.moment_of_inertia # already converted to SI in Lib.py

        self.freqs = self.freq_step.harmonic_freqs
        self.freqs = [x*100*c for x in self.freqs] # cm-1 --> Hz
        self.real_freqs = [x for x in self.freqs if x>0]

        self.method=unify_method(self.freq_step.method)
        self.basis=unify_basis(self.freq_step.basis)

        self.temp=temp
        self.pressure=pressure
        self.ZPE_factor=ZPE_factor
        self.H_factor=H_factor
        self.S_factor=S_factor
        self.factor_source=factor_source
        self.same_for_all=True

        if not manual_specify and self.method and self.basis:
            for line in self.factor_database:
                if self.method==line['Method'][1] and self.basis==line['Basis'][1]:
                    scaling_line=line
                    print(line)
                    print("Please Verify:")
                    print("Automatic search result of scaling factor for METHOD:",scaling_line['Method'][0]," BASIS:",scaling_line['Basis'][0])
                    ZPE_factor = scaling_line['ZPE']
                    S_factor = scaling_line['S(T)']
                    H_factor = scaling_line['H(T)-H(0)']
                    factor_source = scaling_line['Source']
                    print("Scaling factors: ZPE:",ZPE_factor,' H(0->T):',H_factor," S(T):",S_factor,' Source:',factor_source)


                    thermo_factor_dialog = Thermo_Factor_Form(self.freq_step.filename,
                                                              self.freq_step.method,
                                                              self.freq_step.basis,
                                                              scaling_line['Method'][0],
                                                              scaling_line['Basis'][0],
                                                              self.freq_step.temp,
                                                              self.freq_step.pressure,
                                                              ZPE_factor,
                                                              H_factor,
                                                              S_factor,
                                                              factor_source)

                    thermo_factor_dialog.exec_()
                    self.temp = thermo_factor_dialog.temp
                    self.pressure = thermo_factor_dialog.pressure
                    self.ZPE_factor=thermo_factor_dialog.ZPE_factor
                    self.H_factor=thermo_factor_dialog.H_factor
                    self.S_factor=thermo_factor_dialog.S_factor
                    self.factor_source=thermo_factor_dialog.source
                    self.same_for_all = thermo_factor_dialog.same_for_all

                    break

        if self.temp==-1:
            self.temp = self.freq_step.temp

        if self.pressure==-1:
            self.pressure = self.freq_step.pressure*atm__Pa

        self.scaling()

    def translate(self):

        T = self.temp
        P = self.pressure
        m = self.mass

        q = ((2*pi*m*k_B*T/h**2)**(3/2))*R*T/P

        trans = {}
        trans['q'] = q
        trans['q/N'] = q/N_A

        trans['C_V'] = 3/2 * R
        trans['C_P'] = 5/2 * R

        trans['U(T)'] = 3/2 * R * T
        trans['H(T)'] = 5/2 * R * T

        trans['S(T)'] = R * ( ln(q/N_A) + 5/2 )

        self.translation_dict = trans

        # print(trans)

    def rotation(self):
        T = self.temp
        I_A, I_B, I_C = self.moments_of_inertia
        sigma = self.rotation_symm_number
        # print(I_A,I_B,I_C,sigma)

        rotation = {}

        if self.is_linear:
            q = 8*pi**2*I_A*k_B*T / sigma / h**2

            rotation['q'] = q
            rotation['U(T)'] = R*T
            rotation['C_V'] = R
            rotation['S(T)'] = R*( ln(q) + 1 )

        else:
            q = (8*pi**2 / sigma / h**3)*(2*pi*k_B*T)**(3/2)*sqrt(I_A*I_B*I_C)
            rotation['q'] = q
            rotation['U(T)'] = 3/2*R*T
            rotation['C_V'] = 3/2*R
            rotation['S(T)'] = R*( ln(q) + 3/2 )

        self.rotation_dict = rotation








        # print(rotation)

    def vibration(self):
        T = self.temp

        vibration = {key:[] for key in ['q(BOT)', 'q(V=0)',
                                        'U(0)','ZPE', 'U(0->T)', "U(T)",
                                        'C_V',
                                        'S(T)']}

        for nu in self.real_freqs:

            reduced_freq = h*nu/k_B/T
            freq_exp = exp(-reduced_freq)
        
            vibration['q(BOT)'  ].append(sqrt(freq_exp)/(1-freq_exp))
            vibration['q(V=0)'  ].append(1/(1-freq_exp))
            vibration['U(0)'    ].append(1/2*h*nu*N_A)
            vibration['U(0->T)' ].append(R*T*(reduced_freq)*(freq_exp/(1-freq_exp)))
            vibration['U(T)'    ].append(vibration['U(0->T)'][-1]+vibration['U(0)'][-1])
            vibration['C_V'     ].append(R*(reduced_freq)**2 * (freq_exp/(1-freq_exp)**2))
            vibration['S(T)'    ].append(R*( (reduced_freq) * (freq_exp/(1-freq_exp)) - ln(1-freq_exp)))

        vibration['ZPE'] = vibration['U(0)']

        self.vibration_dict = vibration

        # for items in self.vibration_dict.items():
        #     print(items)

    def electronic(self):
        self.electronic_dict = {"q"     :self.multiplicity,
                                'S(T)'  :R*ln(self.multiplicity)}

    def total(self):

        from numpy import prod
        self.translate()
        self.rotation()
        self.vibration()
        self.electronic()

        T = self.temp

        trans = self.translation_dict
        rot = self.rotation_dict
        vib = self.vibration_dict
        elec = self.electronic_dict

        total = {}
        total['q(BOT)'  ] = trans['q/N'] * rot['q'] * prod(vib['q(BOT)']) * elec['q']
        total['q(V=0)'  ] = trans['q/N'] * rot['q'] * prod(vib['q(V=0)']) * elec['q']
        total['C_V'     ] = trans['C_V']+rot['C_V']+sum(vib['C_V'])
        total['C_P'     ] = total['C_V'] + R
        total['S(T)'    ] = trans['S(T)']+rot['S(T)']+sum(vib['S(T)'])+elec['S(T)']

        total["ZPE" ] = sum(vib['ZPE'])
        total['U(T)'] = trans['U(T)']+rot['U(T)']+sum(vib['U(T)'])
        total['H(T)'] = total['U(T)'] + R*T
        total['G(T)'] = total['H(T)'] - T*total['S(T)']
        total['H(0->T)'] = total["H(T)"]-total['ZPE']

        self.total_dict=total



    # def print_total(self):
